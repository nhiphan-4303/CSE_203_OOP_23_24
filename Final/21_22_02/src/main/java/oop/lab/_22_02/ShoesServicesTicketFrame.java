    private String buildTicketForm(int ticketId, String customerName, String brand, String model, String type, String color, String services, float payment) {
        return "Ticket ID: \t" + ticketId + "\n"
                + "Customer Name: \t" + customerName + "\n"
                + "Brand: \t" + brand + "\n"
                + "Model: \t" + model + "\n"
                + "Type: \t" + type + "\n"
                + "Color: \t" + color + "\n"
                + "Services: \t" + services + "\n"
                + "-----*-----Payment: \t" + payment + "\n";
    }

    private String getShoesServices() {
        StringBuilder services = new StringBuilder();

        if (washingCheckbox.isSelected()) {
            services.append("Washing, ");
        }
        if (renewUpperCheckbox.isSelected()) {
            services.append("Renew Upper, ");
        }
        if (renewSoleCheckbox.isSelected()) {
            services.append("Renew Sole, ");
        }
        if (repairCheckbox.isSelected()) {
            services.append("Repair, ");
        }
        if (customPaintCheckbox.isSelected()) {
            services.append("Custom Paint, ");
        }

        if (services.length() > 0) {
            services.setLength(services.length() - 2);
        }

        return services.toString();

    }

    private String getShoesType() {
        return typeCmb.getSelectedItem().toString();
    }

    private boolean isFill() {
        if (ticketIdTxt.getText().trim().isEmpty()
                || customerNameTxt.getText().trim().isEmpty()
                || brandTxt.getText().trim().isEmpty()
                || modelTxt.getText().trim().isEmpty()
                || colorTxt.getText().trim().isEmpty()
                || laborPriceTxt.getText().trim().isEmpty()
                || voucherTxt.getText().trim().isEmpty()) {
            return false;
        }
        return true;
    }

    private void clearForm() {
        ticketIdTxt.setText("");
        customerNameTxt.setText("");
        brandTxt.setText("");
        modelTxt.setText("");
        colorTxt.setText("");
        laborPriceTxt.setText("");
        typeCmb.setSelectedIndex(0);
        washingCheckbox.setSelected(false);
        renewUpperCheckbox.setSelected(false);
        renewSoleCheckbox.setSelected(false);
        repairCheckbox.setSelected(false);
        customPaintCheckbox.setSelected(false);
    }

