    private void printBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printBtnActionPerformed

        if (!isFill()) {
            JOptionPane.showMessageDialog(this, "Please fill all the fields");
            return;
        }

        try {
            int ticketId = Integer.parseInt(ticketIdTxt.getText());
            float laborPrice = Float.parseFloat(laborPriceTxt.getText());
            float voucher = Float.parseFloat(voucherTxt.getText());

            String customerName = customerNameTxt.getText();
            String brand = brandTxt.getText();
            String model = modelTxt.getText();
            String color = colorTxt.getText();

            String type = getShoesType();
            String services = getShoesServices();

            ShoesServicesTicket ticket = new ShoesServicesTicket(ticketId, customerName, brand, model, color, type, laborPrice, services, voucher);

            float payment = ticket.getPayment();

            ticketInfoTxtArea.setText(buildTicketForm(ticketId, customerName, brand, model, type, color, services, payment));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please check your numbbers!");
        }
    }//GEN-LAST:event_printBtnActionPerformed

