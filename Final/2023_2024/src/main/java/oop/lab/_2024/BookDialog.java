    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        int selectedRowIndex = bookTable.getSelectedRow();

        int bookId = (int) bookTable.getValueAt(selectedRowIndex, 0);
        Book bookToEdit = bookList.findById(bookId);

        if (bookToEdit != null) {
            idTxt.setText(bookTable.getValueAt(selectedRowIndex, 0).toString());
            titleTxt.setText(bookTable.getValueAt(selectedRowIndex, 1).toString());
            authorNameTxt.setText(bookTable.getValueAt(selectedRowIndex, 2).toString());
            yearTxt.setText(bookTable.getValueAt(selectedRowIndex, 3).toString());
            totalPageTxt.setText(bookTable.getValueAt(selectedRowIndex, 4).toString());
            priceTxt.setText(bookTable.getValueAt(selectedRowIndex, 5).toString());

            idTxt.setEditable(false);
        } else {
            JOptionPane.showMessageDialog(this, "Can not find this book!");
        }
    }//GEN-LAST:event_editBtnActionPerformed

