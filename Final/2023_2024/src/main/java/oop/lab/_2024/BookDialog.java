    private void updateTable() {
        DefaultTableModel model = (DefaultTableModel) bookTable.getModel();

        model.setRowCount(0);
        for (Book b : bookList.getBookList()) {
            Object[] rowData = new Object[]{
                b.getId(),
                b.getTitle(),
                b.getAuthorName(),
                b.getPublishedYear(),
                b.getTotalPages(),
                b.getPrice()
            };
            model.addRow(rowData);
        }
        pack();
    }

    private boolean isFill() {
        return !(idTxt.getText().trim().isEmpty()
                || titleTxt.getText().trim().isEmpty()
                || authorNameTxt.getText().trim().isEmpty()
                || yearTxt.getText().trim().isEmpty()
                || totalPageTxt.getText().trim().isEmpty()
                || priceTxt.getText().trim().isEmpty());
    }

