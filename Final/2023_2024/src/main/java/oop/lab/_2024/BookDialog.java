    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed

        if (!isFill()) {
            JOptionPane.showMessageDialog(this, "Please check all the fields!");
            return;
        }
        try {

            int id = Integer.parseInt(idTxt.getText());
            int year = Integer.parseInt(yearTxt.getText());
            int pages = Integer.parseInt(totalPageTxt.getText());
            float price = Float.parseFloat(priceTxt.getText());

            String title = titleTxt.getText();
            String authorName = authorNameTxt.getText();

            Book book = new Book(id, title, authorName, year, pages, price);

            if (bookList.checkId(id)) {
                bookList.getBookList().add(book);
                JOptionPane.showMessageDialog(this, "Add book sucessfully!");
            } else {
                JOptionPane.showMessageDialog(this, "ID already exists. Please use a unique ID.");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please check your numbers!");
        }
    }//GEN-LAST:event_addBtnActionPerformed

