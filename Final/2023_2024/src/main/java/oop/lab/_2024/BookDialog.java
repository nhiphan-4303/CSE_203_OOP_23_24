    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int selectedRowIndex = bookTable.getSelectedRow();
        if (selectedRowIndex >= 0) {
            int confirm = JOptionPane.showConfirmDialog(this, "Do you want to delete?", "Delete", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                int bookId = (int) bookTable.getValueAt(selectedRowIndex, 0);
                Book bookToDelete = bookList.findById(bookId);

                if (bookToDelete != null) {
                    bookList.getBookList().remove(bookToDelete);
                    JOptionPane.showMessageDialog(this, "Delete Sucessfully!");
                    updateTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Can not find this book!");
                }

            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

