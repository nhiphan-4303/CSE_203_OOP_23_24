    private void updateTable() {
        LocalDateTime start = LocalDateTime.parse(startDateComboBox.getSelectedItem().toString());
        LocalDateTime end = LocalDateTime.parse(endDateComboBox.getSelectedItem().toString());

        setTable(start, end);
    }

    private void setTable(LocalDateTime start, LocalDateTime end) {
        DefaultTableModel model = (DefaultTableModel) revenueTable.getModel();
        model.setRowCount(0);

        float totalRevenue = 0;

        ArrayList<LocalDateTime> datesInRange = new ArrayList<>();

        for (LocalDateTime date : manageMainBill.getAllDates()) {
            if (!date.isBefore(start) && !date.isAfter(end)) {
                datesInRange.add(date);
            }
        }

        for (LocalDateTime date : datesInRange) {

            for (MainBill bill : manageMainBill.getListMainBills()) {
                if (bill.getDateTime().toLocalDate().isEqual(date.toLocalDate())) {
                    String customerName = bill.getCustomerName();

                    float grandTotal = bill.getGrandTotal();
                    int quantity = 0;
                    for (FoodBill f : bill.getItems()) {
                        quantity += f.getOrderQuantity();
                    }

                    totalRevenue += grandTotal;

                    Object[] rows = new Object[]{date.toLocalDate(), customerName, quantity, grandTotal};
                    model.addRow(rows);

                }
            }
        }

        totalField.setText(String.format("%.2f", totalRevenue));

        pack();

    }

    public void setDateComboBox() {
        startDateComboBox.removeAllItems();
        endDateComboBox.removeAllItems();
        for (MainBill m : manageMainBill.getListMainBills()) {
            startDateComboBox.addItem(String.valueOf(m.getDateTime()));
            endDateComboBox.addItem(String.valueOf(m.getDateTime()));
        }
    }

    public void saveBills() {
        try (ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("Bills.dat"))) {
            oos.writeObject(manageMainBill.getListMainBills());
            JOptionPane.showMessageDialog(this, "Bills saved successfully!");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error saving bill to file: " + e.getMessage());
        }
    }

    public void loadBills() {
        try (ObjectInputStream ois = new ObjectInputStream(new FileInputStream("Bills.dat"))) {
            manageMainBill.setListMainBills((ArrayList<MainBill>) ois.readObject());
        } catch (IOException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "Error loading Bills from file.");
        }
    }

